/*
 * BOOL type is 1 byte long
 * INT type is 4 byte long
 * FLOAT type is 4 byte long
 * CHARBUF type is a fixed character array of a given SIZE in bytes
 * STRING type is a common string type with no need of specify the size
 *
 */

#define BOOL 0
#define INT 1
#define FLOAT 2
#define CHARBUF 3
#define STRING 4
